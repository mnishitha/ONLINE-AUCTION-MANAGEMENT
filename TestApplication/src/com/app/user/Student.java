package com.app.user;

public class Student {
	
	String name;
	int computerMark;
	int mathematicsMark;
	int geographyMark;
	int historyMark;
	int englishMark;
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getComputerMark() {
		return computerMark;
	}
	public void setComputerMark(int computerMark) {
		this.computerMark = computerMark;
	}
	public int getMathematicsMark() {
		return mathematicsMark;
	}
	public void setMathematicsMark(int mathematicsMark) {
		this.mathematicsMark = mathematicsMark;
	}
	public int getGeographyMark() {
		return geographyMark;
	}
	public void setGeographyMark(int geographyMark) {
		this.geographyMark = geographyMark;
	}
	public int getHistoryMark() {
		return historyMark;
	}
	public void setHistoryMark(int historyMark) {
		this.historyMark = historyMark;
	}
	public int getEnglishMark() {
		return englishMark;
	}
	public void setEnglishMark(int englishMark) {
		this.englishMark = englishMark;
	}
	

}
